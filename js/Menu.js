function Mostrar_cesar(){
    document.getElementById('Menu_cesar').style.display='block'
    document.getElementById('menu').style.display= 'none';
}
function Mostrar_viggenere(){
    document.getElementById('Menu_viggenere').style.display = 'block';
    document.getElementById('menu').style.display= 'none';
}
function Mostrar_menu(){
    document.getElementById('Menu_viggenere').style.display = 'none';
    document.getElementById('Menu_cesar').style.display='none'
    document.getElementById('menu').style.display= 'block';
}
